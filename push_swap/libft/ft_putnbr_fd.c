#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char i;

	if (n == -2147483648)
	{
		write(fd, "-", 1);
		write(fd, "2147483648", 10);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		ft_putnbr_fd(n * (-1), fd);
	}
	else
	{
		if (n >= 10)
			ft_putnbr_fd((n / 10), fd);
		i = (n % 10) + '0';
		write(fd, &i, 1);
	}
}
